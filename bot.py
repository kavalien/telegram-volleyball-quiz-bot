async def ask_name(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_id = str(update.effective_user.id)
    username_input = update.message.text.strip()

    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–µ—Ç ‚Äî —Å–æ–∑–¥–∞–¥–∏–º
    if user_id not in scoreboard:
        scoreboard[user_id] = {"username": username_input, "score": 0}
    else:
        scoreboard[user_id]["username"] = username_input

    context.user_data["username"] = username_input

    # –ü—Ä–µ–¥–ª–∞–≥–∞–µ–º –≤—ã–±—Ä–∞—Ç—å –∫–∞—Ç–µ–≥–æ—Ä–∏—é
    categories = list(quiz_data.keys())  # ["–ü—Ä–∞–≤–∏–ª–∞ –≤–æ–ª–µ–π–±–æ–ª–∞ üèê", "–ò—Å—Ç–æ—Ä–∏—è –≤–æ–ª–µ–π–±–æ–ª–∞ üìñ"]
    cat_keyboard = [[cat] for cat in categories]

    await update.message.reply_text(
        f"–ü—Ä–∏—è—Ç–Ω–æ –ø–æ–∑–Ω–∞–∫–æ–º–∏—Ç—å—Å—è, {username_input}!\n–í—ã–±–µ—Ä–∏—Ç–µ –∫–∞—Ç–µ–≥–æ—Ä–∏—é –≤–∏–∫—Ç–æ—Ä–∏–Ω—ã:",
        reply_markup=ReplyKeyboardMarkup(cat_keyboard, one_time_keyboard=True, resize_keyboard=True)
    )
    return CHOOSE_CATEGORY

async def choose_category(update: Update, context: ContextTypes.DEFAULT_TYPE):
    category = update.message.text.strip()
    if category not in quiz_data:
        await update.message.reply_text("–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤—ã–±–µ—Ä–∏—Ç–µ –∫–∞—Ç–µ–≥–æ—Ä–∏—é –∏–∑ —Å–ø–∏—Å–∫–∞.")
        return CHOOSE_CATEGORY

    context.user_data["category"] = category

    questions = quiz_data[category][:]
    random.shuffle(questions)
    for q in questions:
        random.shuffle(q["options"])

    context.user_data["questions"] = questions
    context.user_data["current_question_index"] = 0
    context.user_data["score_this_round"] = 0

    await update.message.reply_text(
        f"–í—ã –≤—ã–±—Ä–∞–ª–∏ –∫–∞—Ç–µ–≥–æ—Ä–∏—é: {category}\n–ù–∞—á–∏–Ω–∞–µ–º –≤–∏–∫—Ç–æ—Ä–∏–Ω—É! –î–ª—è –æ—Ç–º–µ–Ω—ã ‚Äî /cancel."
    )
    return await ask_question(update, context)

