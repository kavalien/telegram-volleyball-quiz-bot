    # –°–±—Ä–æ—Å–∏–º –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
    context.user_data["questions"] = []
    context.user_data["current_question_index"] = 0
    context.user_data["score_this_round"] = 0

    # –í–ù–ò–ú–ê–ù–ò–ï: –≤–æ–∑—Ä–∞—â–∞–µ–º MAIN_MENU –≤–º–µ—Å—Ç–æ ConversationHandler.END
    return MAIN_MENU

